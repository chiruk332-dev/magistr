def _text_pool(symbols, cat_names):
    pool = []
    for kq in KNOWLEDGE_QUESTIONS:
        opts = kq['distractors'][:3] + [kq['correct']]
        random.shuffle(opts)
        pool.append({'type': 'text', 'text': kq['text'], 'image': None,
                     'correct': kq['correct'], 'options': opts})
    for sym in random.sample(symbols, min(160, len(symbols))):
        cat = sym.category.name if sym.category else 'Інші'
        wrong = [c for c in cat_names if c != cat]
        if len(wrong) < 3:
            continue
        opts = random.sample(wrong, 3) + [cat]
        random.shuffle(opts)
        pool.append({
            'type': 'text',
            'text': f'До якої категорії документу належить знак «{sym.name[:90]}»?',
            'image': None, 'correct': cat, 'options': opts,
        })
    return pool


def _sign_pool(symbols):
    pool = []
    for sym in random.sample(symbols, min(220, len(symbols))):
        wrong = random.sample([s for s in symbols if s.id != sym.id], 3)
        if random.random() < 0.5:
            opts = [s.name for s in wrong] + [sym.name]
            random.shuffle(opts)
            pool.append({
                'type': 'image_to_name',
                'text': 'Як називається поданий умовний знак?',
                'image': sym.image_path,
                'correct': sym.name, 'options': opts,
            })
        else:
            opts = [{'id': s.id, 'image_path': s.image_path} for s in wrong] + \
                   [{'id': sym.id, 'image_path': sym.image_path}]
            random.shuffle(opts)
            pool.append({
                'type': 'name_to_image',
                'text': f'Оберіть умовний знак «{sym.name[:90]}»',
                'image': None,
                'correct': sym.image_path, 'options': opts,
            })
    return pool


@quiz_bp.get('/quiz/random-symbol')
def random_symbol():
    symbols = Symbol.query.all()
    s = random.choice(symbols)
    return jsonify(s.to_dict())


@quiz_bp.get('/quiz/image-choice')
def image_choice():
    symbols = Symbol.query.filter(Symbol.image_path != '/static/symbols/placeholder.svg').all()
    correct = random.choice(symbols)
    wrong = random.sample([s for s in symbols if s.id != correct.id], 3)
    options = [{'id': s.id, 'image_path': s.image_path} for s in wrong + [correct]]
    random.shuffle(options)
    return jsonify({
        'name': correct.name,
        'category': correct.category.name if correct.category else '',
        'correct_id': correct.id,
        'options': options,
    })


@quiz_bp.get('/quiz/control-questions')
def control_questions():
    symbols = Symbol.query.filter(Symbol.image_path != '/static/symbols/placeholder.svg').all()
    categories = Category.query.all()
    cat_names = [c.name for c in categories]

    text_q = random.sample(_text_pool(symbols, cat_names), 8)   # 40%
    sign_q = random.sample(_sign_pool(symbols), 12)              # 60%

    combined = text_q + sign_q
    random.shuffle(combined)
    return jsonify(combined)
